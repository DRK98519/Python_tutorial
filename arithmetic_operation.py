## Arithmetic Operation

print(10 + 3)   # Addition
print(10 - 3)   # Subtraction
print(10 * 3)   # Multiplication
print(10 / 3)   # Regular Division
print(10 // 3)  # Division rounded to integer
print(10 % 3)   # Modulus, returns remainder of division
print(10 ** 3)  # Exponent (power), 10 ^ 3 here


# Augmented assignment operator
x = 10
x += 3  # Augmented asssignment operator used, same as x = x + 3. Similarly, we have -= *= /= //= etc
print(x)