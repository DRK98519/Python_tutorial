# Import math module for complex mathematical operation in Python

import math # import math module into the code, the module contains many methods in the syntax math.[method()]
print(math.ceil(2.9))  # Round a number upward to its nearest integer
print(math.floor(2.9))  # Round a number downward to its nearest integer

